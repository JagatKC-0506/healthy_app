// Get random meal for each time
export function getRandomMeal(mealType) {
  const meals = nepaliDietData[mealType];
  return meals[Math.floor(Math.random() * meals.length)];
}

// Get today's meal plan (seeded by date for consistency)
export function getTodaysMealPlan() {
  const today = new Date();
  const seed = today.getDate() + today.getMonth() * 31;
  
  return {
    breakfast: nepaliDietData.breakfast[seed % nepaliDietData.breakfast.length],
    lunch: nepaliDietData.lunch[(seed + 1) % nepaliDietData.lunch.length],
    snack: nepaliDietData.snacks[(seed + 2) % nepaliDietData.snacks.length],
    dinner: nepaliDietData.dinner[(seed + 3) % nepaliDietData.dinner.length]
  };
}

// Calculate daily nutrition
export function calculateDailyNutrition(mealPlan) {
  const meals = [mealPlan.breakfast, mealPlan.lunch, mealPlan.snack, mealPlan.dinner];
  return meals.reduce((acc, meal) => ({
    calories: acc.calories + meal.calories,
    protein: acc.protein + meal.protein
  }), { calories: 0, protein: 0 });
}
